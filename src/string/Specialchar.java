package string;

public class Specialchar {
    public static void main(String[]args){
        String x="\'sourav\'";
        System.out.println(x);
        String y="\"sourav\"";
        System.out.println(y);
        String z="\\sourav";
        System.out.println(z);
        String a = "Hello\nWorld!";
        System.out.println(a);

    }
}
